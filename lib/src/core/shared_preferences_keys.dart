/// Keys used to persist values in `SharedPreferences`.
abstract final class SharedPreferencesKeys {
  /// Selected app theme.
  static const themeKey = 'theme';

  /// Selected app language.
  static const languageKey = 'language';

  /// Selected code syntax theme for dark mode.
  static const codeDarkThemeKey = 'code_dark_theme';

  /// Selected code syntax theme for light mode.
  static const codeLightThemeKey = 'code_light_theme';
}
