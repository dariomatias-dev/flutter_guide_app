/// Named identifiers for the app's routes.
abstract final class RouteNames {
  /// Component detail route name.
  static const component = 'component';

  /// Interface catalog route name.
  static const catalog = 'catalog';

  /// Component sample viewer route name.
  static const componentSample = 'component-sample';

  /// Saved components route name.
  static const savedComponents = 'saved-components';

  /// Code syntax theme selector route name.
  static const codeTheme = 'code-theme';
}
